////////////////////////////////////////////////////////////////////////////////
// MARK: Imports
import Foundation

////////////////////////////////////////////////////////////////////////////////
// MARK: Types


/**
 *  Player Model
 */
class Player {

    ////////////////////////////////////////////////////////////////////////////////
    // MARK: Private Properties

    ////////////////////////////////////////////////////////////////////////////////
    // MARK: Public Properties
    
    var id : String?
    var jerseyNumber : String?
    var person : Person?
    
    ////////////////////////////////////////////////////////////////////////////////
    // MARK: Setup & Teardown

    ////////////////////////////////////////////////////////////////////////////////
    // MARK: Class Methods

    ////////////////////////////////////////////////////////////////////////////////
    // MARK: Override Methods

    ////////////////////////////////////////////////////////////////////////////////
    // MARK: Private Methods

    ////////////////////////////////////////////////////////////////////////////////
    // MARK: public Methods
    
    func hidrate(fromDict:NSDictionary) -> Player {
        if let id = fromDict["Id"] as? String {
            self.id = id
        }
        if let jerseyNumber = fromDict["JerseyNumber"] as? String {
            self.jerseyNumber = jerseyNumber
        }
        if let person = fromDict["Person"] as? NSDictionary {
            self.person = Person().hidrate(person)
        }
        return self
    }
}
